async def seed_flowers(db: AsyncSession) -> None:
    result = await db.execute(select(Flower))
    existing = result.scalars().all()

    if not existing:
        for f in INITIAL_FLOWERS:
            db.add(Flower(**f))
        await db.commit()
        return

    # DB already has flowers: keep ids, but sync text/image fields by latin_name.
    existing_by_latin = {f.latin_name: f for f in existing if f.latin_name}
    for item in INITIAL_FLOWERS:
        latin = item.get("latin_name")
        if latin and latin in existing_by_latin:
            flower = existing_by_latin[latin]
            flower.name = item["name"]
            flower.description = item["description"]
            flower.image_url = item["image_url"]
            flower.season = item["season"]

    await db.commit()